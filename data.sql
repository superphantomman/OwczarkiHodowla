SELECT p.id, pk.id,
    CASE WHEN RAND() < 0.33 THEN 'zadna'
         WHEN RAND() < 0.66 THEN 'ograniczona'
         ELSE 'dowolna'
    END,
	(CAST(CAST ( ROUND(RAND() * 10000.0, 0) AS INT)  % 2 AS BIT)) 
FROM hodowla.Pieski p
CROSS JOIN hodowla.Pokarm pk

